def volume_balok(x,y,z):
    volume_balok = (x*y*z)
    return volume_balok

def volume_kubus(x):
    volume_kubus = (x*x*x)
    return volume_kubus

def volume_limas_segiempat(x,y):
    volume_limas_segiempat = 0,33*(x*y)
    return volume_limas_segiempat

def volume_bola(x):
    volume_bola = 0,75*3,14*(x**3)
    return volume_bola